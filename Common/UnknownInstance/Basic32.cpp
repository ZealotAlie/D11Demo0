#include "Basic32.h"

static const D3D11_INPUT_ELEMENT_DESC Basic32LayoutDesc[3] = 
{
	{"POSITION", 0, DXGI_FORMAT_R32G32B32_FLOAT, 0, 0, D3D11_INPUT_PER_VERTEX_DATA, 0},
	{"NORMAL",   0, DXGI_FORMAT_R32G32B32_FLOAT, 0, 12, D3D11_INPUT_PER_VERTEX_DATA, 0},
	{"TEXCOORD", 0, DXGI_FORMAT_R32G32_FLOAT, 0, 24, D3D11_INPUT_PER_VERTEX_DATA, 0}
};

class VertexInputLayout:public ResourceInstance< VertexInputLayout, ID3D11InputLayout >
{
public:
	VertexInputLayout(ID3D11Device* device)
	{
		D3DX11_PASS_DESC passDesc;

		BasicEffect::Ptr()->Light1Tech->GetPassByIndex(0)->GetDesc(&passDesc);

		HR(device->CreateInputLayout(Basic32LayoutDesc,3,passDesc.pIAInputSignature,passDesc.IAInputSignatureSize,&m_pResource));
	}
};

ID3D11InputLayout* Basic32::GetInputLayout()
{
	return VertexInputLayout::Ptr();
}

